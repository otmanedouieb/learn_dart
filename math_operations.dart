import 'dart:math';

void main() {
  /**
   * In this section we learn how to use math operations
   */
  print("Simple math operations:");
  // Addition
  print(1 + 1);
  // Subtraction
  print(5 - 3);
  // Multiplication
  print(5 * 3);
  // Division
  print(5 / 3);

  print("Use math lib:");
  // power
  print(pow(2, 3));
}
